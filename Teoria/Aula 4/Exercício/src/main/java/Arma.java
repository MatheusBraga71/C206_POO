public class Arma{

    String nome;
    int poder;
    int resistencia;
    String descricao;

    void mostraInfoArma()
    {
        System.out.println("Nome da Arma: " + nome);
        System.out.println("Poder da Arma: " + poder);
        System.out.println("Resistência da Arma: " + resistencia);
        System.out.println("Descrição da Arma: " + descricao);
    }

}
